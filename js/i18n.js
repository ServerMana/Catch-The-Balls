export function applyTranslations(lang) {
  const t = translations[lang] || translations.ko;
  const $ = (id) => document.getElementById(id);
  const setText = (id, text) => { const el = $(id); if (el) el.textContent = text; };

  setText('startTitle', t.startTitle);
  setText('startDesc', t.startDesc);
  setText('labelLanguage', t.labelLanguage);
  setText('labelMode', t.labelMode);
  setText('labelLives', t.labelLives);
  setText('labelDifficulty', t.labelDifficulty);
  setText('labelTimeLimit', t.labelTimeLimit);
  setText('startBtn', t.startBtn);
  setText('scoreLabel', t.scoreLabel);
  setText('livesLabel', t.livesLabel);
  setText('timeLabel', t.timeLabel);
  setText('gameOverTitle', t.gameOverTitle);
  setText('gameOverSubtitle', t.gameOverSubtitle);

  // 옵션 텍스트도 갱신
  const modeSelect = document.getElementById('modeSelect');
  if (modeSelect) {
    modeSelect.options[0].text = t.optionStandard;
    modeSelect.options[1].text = t.optionInfinite;
    modeSelect.options[2].text = t.optionTimed;
  }
  const difficultySelect = document.getElementById('difficultySelect');
  if (difficultySelect) {
    difficultySelect.options[0].text = t.optionEasy;
    difficultySelect.options[1].text = t.optionNormal;
    difficultySelect.options[2].text = t.optionHard;
    difficultySelect.options[3].text = t.optionCustom;
  }
  
  setText('labelSpawnSpeed', t.labelSpawnSpeed);
  setText('labelFallSpeed', t.labelFallSpeed);
  setText('labelBallSize', t.labelBallSize);
  setText('labelCustomSpawn', t.labelCustomSpawn);
  setText('labelCustomSpeed', t.labelCustomSpeed);
  setText('labelCustomSizeMin', t.labelCustomSizeMin);
  setText('labelCustomSizeMax', t.labelCustomSizeMax);
  setText('quitBtnText', t.quitBtn);
  setText('advancedToggleText', t.advancedToggleText);
  setText('advancedTitle', t.advancedTitle);
  setText('labelScreenSize', t.labelScreenSize);
  setText('labelWidth', t.labelWidth);
  setText('labelHeight', t.labelHeight);
  setText('applyScreenText', t.applyScreenText);
  setText('labelCustomDifficulty', t.labelCustomDifficulty);
  setText('labelBackgroundColor', t.labelBackgroundColor);
  setText('labelCustomColor', t.labelCustomColor);
  setText('labelScoreSystem', t.labelScoreSystem);
  setText('scoreVerySmall', t.scoreVerySmall);
  setText('scoreSmall', t.scoreSmall);
  setText('scoreMedium', t.scoreMedium);
  setText('scoreLarge', t.scoreLarge);
  setText('scoreVeryLarge', t.scoreVeryLarge);
  setText('pointsUnit1', t.pointsUnit);
  setText('pointsUnit2', t.pointsUnit);
  setText('pointsUnit3', t.pointsUnit);
  setText('pointsUnit4', t.pointsUnit);
  setText('pointsUnit5', t.pointsUnit);
  setText('presetFullHD', t.presetFullHD);
  setText('presetHD', t.presetHD);
  setText('preset2K', t.preset2K);
  setText('presetWindow', t.presetWindow);
  setText('windowSizeText', t.windowSizeText);
  setText('restartBtnText', t.restartBtnText);
}
